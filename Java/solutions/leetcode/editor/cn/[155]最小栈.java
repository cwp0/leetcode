import java.util.*;
class MinStack {
    Stack<Integer> stack;
    // 记录每次插入后的最小元素
    Stack<Integer> minStack;
    public MinStack() {
        stack = new Stack<>();
        minStack = new Stack<>();
    }
    
    public void push(int val) {
        stack.push(val);
        if (minStack.isEmpty() || val < minStack.peek()) {
            // 存最小元素的栈是空的或者当前插入的值小于最小栈的栈顶元素
            minStack.push(val);
        } else {
            // 当前插入的val大于最小栈的栈顶元素
            minStack.push(minStack.peek());
        }
    }
    
    public void pop() {
        stack.pop();
        minStack.pop();
    }
    
    public int top() {
        return stack.peek();
    }
    
    public int getMin() {
        return minStack.peek();
    }
}

