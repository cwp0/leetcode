class Solution {
    public int maxCoins(int[] nums) {
        int n = nums.length;
        int[] points = new int[n+2];
        points[0] = 1;
        points[n+1] = 1;
        for (int i = 1; i <= n; i++) {
            points[i] = nums[i-1];
        }
        // base caseä¸º j <= i æ—¶ï¼ŒåŒºé—´å†…æ²¡æœ‰æ²¡æœ‰åŒºé—´å¯ä»¥æ‰ç ´ï¼Œå·²ç»è¢«åˆå§‹åŒ–ä¸º0
        // å®šä¹‰dp[i][j]ä¸ºæ‰ç ´å¼€åŒºé—´(i, j)å†…çš„æ‰€æœ‰åŒºé—´è·å¾—çš„ç¡¬å¸ã€‚
        int[][] dp = new int[n+2][n+2];
        // i ä»ä¸‹å¾€ä¸Šéå†
        for (int i = n; i >= 0; i--) {
            // j ä»å·¦å¾€å³éå†
            for (int j = i+1; j <= n+1; j++) {
                for (int k = i+1; k < j; k++) {
                    dp[i][j] = Math.max(dp[i][j], dp[i][k] + dp[k][j] + points[i]*points[k]*points[j]);
                }
            }
        }

        return dp[0][n+1];
    }


}
