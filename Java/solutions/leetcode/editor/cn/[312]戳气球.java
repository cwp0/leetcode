class Solution {
    public int maxCoins(int[] nums) {
        int n = nums.length;
        int[] air_balls = new int[n+2];
        for (int i = 0; i <= n+1; i++) {
            if (i == 0 || i == n+1) {
                air_balls[i] = 1;
                continue;
            }
            air_balls[i] = nums[i-1];
        }
        // å®šä¹‰dp[i][j]ä¸ºæˆ³ç ´å¼€åŒºé—´(i, j)å¯ä»¥è·å¾—çš„æœ€å¤§ç¡¬å¸æ•°ï¼Œæ‰€ä»¥è¦æ±‚çš„æ˜¯dp(0, n+1)
        int[][] dp = new int[n+2][n+2];

        for (int i = n; i >= 0; i--) {
            for (int j = i+1; j < n+2; j++) {
                for (int k = i+1; k < j; k++) {
                    dp[i][j] = Math.max(dp[i][j], dp[i][k] + dp[k][j] + air_balls[i]*air_balls[k]*air_balls[j]);
                }
            }
        }
        return dp[0][n+1];
    }

}
