class Solution {
    // è®°å½•ringå­—ç¬¦ä¸²ä¸­æ¯ä¸ªå­—ç¬¦å‡ºç°çš„index
    private HashMap<Character, List<Integer>> map = new HashMap<>();
    //å¤‡å¿˜å½•
    private int[][] memo;
    public int findRotateSteps(String ring, String key) {
        int m = ring.length(), n = key.length();

        // é¦–å…ˆåˆ›å»ºå¤‡å¿˜å½•
        memo = new int[m][n];
        for (int[] row : memo) {
            Arrays.fill(row, -1);
        }

        // å°†ringçš„æ‰€æœ‰å­—ç¬¦å‡ºç°çš„indexè®°å½•åœ¨HashMapä¸­ï¼Œå¯ä»¥æ›´å¿«çš„æŸ¥è¯¢
        for (int i = 0; i < m; i++) {
            char c = ring.charAt(i);
            if (!map.containsKey(c)) {
                map.put(c, new LinkedList<>());
            }
            map.get(c).add(i);
        }

        return dp(ring, 0, key, 0);
    }

    // å®šä¹‰dp[i][j]ä¸ºåœ†ç›˜æŒ‡é’ˆåœ¨ring[i]ï¼Œè¾“å…¥key[j..]æ‰€éœ€è¦çš„æœ€å°‘æ¬¡æ•°ã€‚
    private int dp(String ring, int i, String key, int j) {
        int m = ring.length(), n = key.length();
        // base case
        // è¡¨æ˜keyå·²ç»å…¨éƒ¨è¢«è¾“å…¥
        if (j == n) return 0;
        // æŸ¥æ‰¾å¤‡å¿˜å½•ï¼Œé¿å…é‡å¤è®¡ç®—
        if (memo[i][j] != -1) return memo[i][j];

        int res = Integer.MAX_VALUE;
        for (int k : map.get(key.charAt(j))) {
            // ä»iè½¬åŠ¨åˆ°kæ‰€éœ€è¦æŒ‡é’ˆçš„æ‹¨åŠ¨æ¬¡æ•°
            int num = Math.abs(i-k);
            // é¡ºæ—¶é’ˆè½¬ å’Œ é€†æ—¶é’ˆè½¬ å–æœ€å°å€¼
            num = Math.min(num, m - num);
            // å°†ringæŒ‡é’ˆæ‹¨åˆ°kå¤„ï¼Œç»§ç»­è¾“å…¥key[j+1..]
            int subProblem = dp(ring, k, key, j + 1);

            // 1 ä»£è¡¨æ‹¨åŠ¨æŒ‡é’ˆå æŒ‰ä¸‹ä¸­é—´çš„button
            // num ä»£è¡¨å°†ringæŒ‡é’ˆä»iæ‹¨åˆ°kæ‰€éœ€è¦çš„è½¬åŠ¨æ¬¡æ•°
            res = Math.min(res, 1 + num + subProblem);
        }
        memo[i][j] = res;
        return memo[i][j];
    }
}
