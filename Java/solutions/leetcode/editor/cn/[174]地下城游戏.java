class Solution {
    public int calculateMinimumHP(int[][] dungeon) {
        int m = dungeon.length, n = dungeon[0].length;
        int[][] dp = new int[m+1][n+1];
        // base case
        for (int i = 0; i <= m; i ++) {
            dp[i][n] = Integer.MAX_VALUE;
        }
        for (int j = 0; j <= n; j ++) {
            dp[m][j] = Integer.MAX_VALUE;
        }
        dp[m-1][n-1] = dungeon[m - 1][n - 1] < 0 ?  1 - dungeon[m - 1][n - 1] : 1;  // ä¿è¯æœ€ä½è¡€é‡ä¸ºæ­£æ•°

        for (int i = m - 1; i >= 0; i --) {
            for (int j = n - 1; j >= 0; j --) {
                if (i == m - 1 && j == n - 1) continue;

                int val = Math.min(dp[i+1][j], dp[i][j+1]) - dungeon[i][j];
                dp[i][j] = val <= 0 ? 1 : val;  // ä¿è¯å‡ºç”Ÿåœ¨[i,j]çš„æœ€ä½è¡€é‡ä¸ºæ­£æ•°
            }
        }
        return dp[0][0];
    }
}
