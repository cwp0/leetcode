class Solution {
    private int[][] memo;
    public int calculateMinimumHP(int[][] dungeon) {
        int m = dungeon.length, n = dungeon[0].length;
        memo = new int[m][n];
        for (int[] row : memo) {
            Arrays.fill(row, -1);
        }
        return dp(dungeon, 0, 0);
    }

    // å®šä¹‰dp[i][j]ä¸ºä»(i, j)åˆ°å³ä¸‹è§’ï¼Œéœ€è¦çš„æœ€å°‘è¡€é‡æ˜¯å¤šå°‘
    private int dp(int[][] grid, int i, int j) {
        int m = grid.length;
        int n = grid[0].length;
        // base case
        if (i == m - 1 && j == n - 1) {
            return grid[i][j] >= 0 ? 1 : -grid[i][j] + 1;
        }
        // å¤„ç†è¾¹ç•Œ
        if (i == m || j == n) {
            return Integer.MAX_VALUE;
        }
        // é¿å…é‡å¤è®¡ç®—
        if (memo[i][j] != -1) {
            return memo[i][j];
        }
        // çŠ¶æ€è½¬ç§»å‡½æ•°
        int res = Math.min(dp(grid, i+1, j), dp(grid, i, j+1)) - grid[i][j];
        memo[i][j] = res > 0 ? res : 1;

        return memo[i][j];
    }

}
