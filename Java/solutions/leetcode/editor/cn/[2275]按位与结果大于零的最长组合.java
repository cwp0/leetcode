class Solution {
    public int largestCombination(int[] candidates) {
        int[] cnt = new int[24];
        for (int x : candidates) {
            for (int i = 0; x > 0; i++) {
                cnt[i] += x & 1;
                x >>= 1;
            }
        }
        return Arrays.stream(cnt).max().getAsInt();
    }
}
